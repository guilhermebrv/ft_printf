#include "libft.h"

int	ft_isalpha(int c)
{
	if ((c >= 'a' && c <= 'z') || (c >= 'A' && c <= 'Z'))
		return (1);
	return (0);
}

/* checks if the parameter int is an alphabetic character, if its true it
returns 1, if its false it returns 0 */