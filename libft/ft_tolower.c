#include "libft.h"

int	ft_tolower(int c)
{
	if (c >= 'A' && c <= 'Z')
		return (c + 32);
	return (c);
}

/* The function converts an upper-case letter to the corresponding
lower-case letter, returning it. If the argument is not an upper-case
letter, it is returned unchanged.*/